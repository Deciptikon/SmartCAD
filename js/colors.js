export function setColor(r, g, b) {
  return `rgb(${r}, ${g}, ${b})`;
}

export const colorWhite = `rgb(${255}, ${255}, ${255})`;
export const colorRed = `rgb(${255}, ${100}, ${100})`;
export const colorGreen = `rgb(${100}, ${255}, ${100})`;
export const colorBlue = `rgb(${100}, ${100}, ${255})`;
export const colorGray100 = `rgb(${100}, ${100}, ${100})`;

export const colorDashLineBlock = `rgb(${0}, ${0}, ${0})`;
